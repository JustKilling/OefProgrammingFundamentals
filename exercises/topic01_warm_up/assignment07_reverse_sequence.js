"use strict";
/*
# Reverse Sequence
Write a program that reads five numbers (`int`) and
that outputs them in reverse order.

## Example:
### Input
    1
    2
    3
    4
    5

### Output:
    5
    4
    3
    2
    1
*/
import io from "../../utils/io-for-pf.js";

const int1 = io.read("Please provide number 1: ");
const int2 = io.read("Please provide number 2: ");
const int3 = io.read("Please provide number 3: ");
const int4 = io.read("Please provide number 4: ");
const int5 = io.read("Please provide number 5: ");

io.write(int5);
io.write(int4);
io.write(int3);
io.write(int2);
io.write(int1);
